#include "libft.h"

int	ft_nbrlen(long int nbr)
{
	int	len;

	if (nbr < 0)
		nbr *= -1;
	len = 1;
	while (nbr >= 1)
	{
		(nbr /= 10);
		len++;
	}
	return (len);
}
